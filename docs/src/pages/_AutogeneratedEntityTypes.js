import React from 'react';
import { CoordinationType, EntityTypes } from '../../../src/app/constants';
import { DATA_TYPE_ENTITY_TYPES_MAPPING } from '../../../src/app/constant-relationships';
import { DEFAULT_COORDINATION_VALUES } from '../../../src/app/state/coordination';

export function AutogeneratedEntityTypeDefaults() {
  return (
    <table>
      <thead>
        <tr>
          <th>Entity Type</th>
          <th>Default Value</th>
        </tr>
      </thead>
      <tbody>
        {Object.entries(EntityTypes).map(([key]) => (
          <tr key={key}>
            <td><code>{CoordinationType[key]}</code></td>
            <td>{DEFAULT_COORDINATION_VALUES[CoordinationType[key]]}</td>
          </tr>
        ))}
      </tbody>
    </table>
  );
}

export function AutogeneratedEntityTypesPerDataType() {
  return (
    <table>
      <tbody>
        {Object.entries(DATA_TYPE_ENTITY_TYPES_MAPPING).map(([dataType, entityTypes]) => (
          <tr key={dataType}>
            <th><code>{dataType}</code></th>
            <td>
              {entityTypes.map((entityType, i) => (
                <>
                  <code>{entityType}</code>
                  {i < entityTypes.length - 1 ? (<span>,&nbsp;<br /></span>) : null}
                </>
              ))}
            </td>
          </tr>
        ))}
      </tbody>
    </table>
  );
}
